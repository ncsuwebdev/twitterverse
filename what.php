          <h2 class="tweetHeader">What is Twitter?</h2> 
          <p>
          <a href="http://www.twitter.com/">Twitter</a> is a social networking and microblogging service (think tiny blog posts) that lets you answer the question, &quot;What are you doing and thinking?&quot; You send a short text message, 140 characters in length, called &quot;tweets,&quot; to your friends," or &quot;followers." </p>
          <h2>Why Twitter?</h2>
          <p> Although the essential question posed by Twitter (i.e. what are you doing) has never changed, the ways in which Twitterers answer the question continues to evolve. <br />
 Twitter allows users to do more than share simple status updates. Twitterers post links to online resources and engaging Web content, ask and answer questions about almost anything, gather feedback about a product or service, report news as it happens or share thoughts about a current topic or trend. The value of Twitter depends entirely on the quality of the content you contribute and consume.</p>
          <h2>How do I do it? </h2>
          <p> All you need to get started on Twitter is a valid email address. Visit <a href="http://www.twitter.com">www.twitter.com</a> and follow the signup process. If you're setting up an account on behalf of a university entity, try to pick something self-explanatory such as NCStateNews or NCSU_OIT. </p>
          <h2>The Basics</h2>
          <h3><strong>Tweets</strong></h3>
          <p> These are your postsâ€”your answers to the question, What are you doing? Twitter posts are limited to 140 characters (including spaces) so be thoughtful and concise. Unless you elect to protect your updates from public view, your tweets will be available for anyone to see.</p>
          <ul>
 <li>If you're posting a URL , consider using a URL shortening service (<a href="http://tinyurl.com">tinyurl.com</a>, <a href="http://www.bit.ly" target="_blank">bit.ly</a>, <a href="http://www.is.gd" target="_blank">//is.gd</a>, etc.) to limit the character count used up by the link. Avoid vague, misleading or unclear descriptions of any links you post.</li>
 <li>Avoid too much self-promotion unless you're sure your posts will be of interest to your readers and followers.</li>
          </ul>
          <h3>Follow</h3>
          <p>Following other Twitter users and encouraging others to follow you is, to a large extent, what Twitter is all about. To follow someone, select "follow" from that user's page to add their updates to your feed. Some users protect their tweets, so any follow requests must be approved by the user. </p>
          <ul>
 <li>Frequent, relevant and meaningful posts will encourage others to follow your updates. </li>
 <li>Use the list at <a href="../index.php">twitter.ncsu.edu</a> to find new, official NC State feeds you can follow. Take a look at who those people follow as well to identify other valuable, interesting feeds. </li>
 <li>Services such as <a href="http://www.wefollow.com" target="wefollow">wefollow.com</a> can help locate Twitterers interested in similar topics.</li>
 <li>Use <a href="http://search.twitter.com" target="searh">search.twitter.com</a> to find out who's posting about topics relevant to what you're Twittering.</li>
          </ul>
          <h3>Reply</h3>
          <p> Replying allows you to address a Tweet to another Twitter user or users in a post. Placing the @ symbol in front of a user's Twitter name designates a reply. For example, responding to something posted by NC State News Services might read "@NCStateNews great story on research happening at NCSU."</p>
          <ul>
 <li>Since replies appear as regular posts also, they should be used only when the subject of the post is relevant to your other followers as well. If a reply is part of a private conversation, use Twitter's built in direct message tool. </li>
 <li>You can reply to any Twitter user, even if you're not following them.</li>
          </ul>
          <h3>ReTweet</h3>
          <p> When another Twitter user posts something you like or would like to share with your followers, use a "retweet." Designate a retweet by adding the letters RT before a post, then using the @ symbol with the original poster's twitter name. For example: "RT @NCSU_OIT learn to use Twitter lunch and learn on Wednesday."</p>
          <ul>
 <li>ReTweet NC State related content often. </li>
 <li>In the same way you can reply to anyone on Twitter, you can retweet any post, even if you aren't following the original poster. </li>
 <li>ReTweets are a great way to earn new followers and share relevant content with others.</li>
          </ul>
          <h3><strong>Tagging</strong></h3>
          <p> Tagging on Twitter allows you to label or group your tweets on the fly. Prefix any term with the # (hash tag) to designate a tag. For example "Researches discover something really awesome at #NCState" would tag the post #NCState. Tags can be created and used by anyone, and can be about almost anything.</p>
          <ul>
 <li>Only include tags in posts when it's helpful and makes sense. Use tags that are useful and meaningful.</li>
          </ul>
          <h3>Search</h3>
          <p> Search on Twitter works like most other search tools. Using search.twitter.com, you can search directly for tags or topics to see who's saying what across the Twitterverse.</p>
          <ul>
 <li>Search is a great way to find out what others are saying about your brand. Keep an eye on the results and participate in conversations only when it's appropriate. </li>
 <li>To limit your search to specific tags, include the hash tag in your search. For example, to find out what kinds of things people have tagged as NC State, search #NCState. </li>
          </ul>
